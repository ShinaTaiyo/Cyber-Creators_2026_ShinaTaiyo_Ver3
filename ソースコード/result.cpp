//=============================================================
//初期化処理
//=============================================================
HRESULT CResult::Init()
{
	CScene::Init();//シーン初期化処理
	//============================
	//各オブジェクト生成処理
	//============================
	CBg::Create(CBg::BG_RESULT);//リザルト背景を生成	
	//======================================================
	return S_OK;
}
