#ifndef _EDITCAMERA_H_
#define _EDITCAMERA_H_

#include "main.h"

//=======================================================================================================================================================================================================================
// マクロ定義
//=======================================================================================================================================================================================================================
#define EDITCAMERA_POSV_Y	(150.0f)	//視点のY座標
#define EDITCAMERA_POSV_Z	(750.0f)	//視点のZ座標
#define EDITCAMERA_ROLL		(0.04f)		//回転速度
#define EDITCAMERA_SPEED	(10.0f)		//カメラ移動の速度倍率
#define MOUSE_SINCE			(0.1f)		//マウス感度

//=======================================================================================================================================================================================================================
// 構造体
//=======================================================================================================================================================================================================================
typedef struct
{
	D3DXVECTOR3 posV;			//視点
	D3DXVECTOR3 posR;			//注視点
	D3DXVECTOR3 vecU;			//ベクトル
	D3DXVECTOR3 move;			//移動量
	D3DXVECTOR3 rot;			//向き
	D3DXMATRIX mtxProjection;	//プロジェクションマトリックス
	D3DXMATRIX mtxView;			//ビューマトリックス
	int nTurn;					//回転方法　0,公転　1,自転
	float fLength;				//視点と注視点の距離
}EditCamera;

//=======================================================================================================================================================================================================================
// プロトタイプ宣言
//=======================================================================================================================================================================================================================
void InitEditCamera(void);		//初期化処理
void UninitEditCamera(void);	//終了処理
void UpdateEditCamera(void);	//更新処理
void SetEditCamera(void);		//設定処理
EditCamera* GetEditCamera(void);//情報の取得
void SetPosEditCamera(int nCnt);//対象オブジェクトへの移動処理
#endif

